package day2.methods;

public class Example2 {
	public static void add(int x,int y) {
		 
		int result = x+y;
		System.out.println(result);
		}
	
	public static void sub(int x,int y) {
		int result = x-y;
		System.out.println(result);
		}
	
	public static void main(String[] args) {
		add(2,3);
		sub(3,2);
	}

}
