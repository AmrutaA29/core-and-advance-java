package day2.methods;

public class Example5 {
	public static int add(int x,int y) {
		 
		int result = x+y;
		return result;
		}
	
	public static int sub(int x,int y) {
		int result = x-y;
		return result;
		}
	
	public static int mul(int x,int y) {
		int result = x*y;
		return result;
		}
	
	public static int div(int x,int y) {
		int result = x/y;
		return result;
		}
	
}
