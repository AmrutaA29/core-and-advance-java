package day2.methods;

public class Example1 {
	public static void add() {
		int x = 4;
		int y = 5;
		int result = x+y;
		System.out.println(result);
		}
	
	public static void sub() {
		int x = 4;
		int y = 5;
		int result = x-y;
		System.out.println(result);
		}
	
	public static void main(String[] args) {
		add();
		sub();
	}

}
